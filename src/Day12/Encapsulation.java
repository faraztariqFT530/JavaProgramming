package Day12;

public class Encapsulation {

    private String name;
    private int id;
    private String city;

    void setName(String myName){
        name=myName;
    }

    String getName(){
        return name;
    }

    void setId(int myId){
        id=myId;
    }

    int getId(){
        return id;
    }

    void setCity(String myCity){
        city=myCity;
    }

    String getCity(){
        return city;
    }



}
